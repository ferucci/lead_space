<script>
  // Общая функция фильтрации
  function applyFilter(filter) {
    document.querySelectorAll(".case-item").forEach(card => {
      if (filter === "all" || card.dataset.cat === filter) {
        card.style.display = "block";
      } else {
        card.style.display = "none";
      }
    });
  }

  // Десктопные кнопки
  const desktopButtons = document.querySelectorAll(".filters button");
  desktopButtons.forEach(button => {
    button.addEventListener("click", () => {
      const filter = button.dataset.filter;
      applyFilter(filter);
      desktopButtons.forEach(btn => btn.classList.remove("active"));
      button.classList.add("active");
    });
  });

  // Мобильный выпадающий список
  const filterToggle = document.getElementById("filterToggle");
  const filterMenu = document.getElementById("filterMenu");

  if (filterToggle && filterMenu) {
    filterToggle.addEventListener("click", () => {
      filterMenu.classList.toggle("show");
    });

    filterMenu.querySelectorAll("div").forEach(item => {
      item.addEventListener("click", () => {
        const filter = item.dataset.filter;
        applyFilter(filter);
        filterMenu.classList.remove("show");
        filterToggle.innerText = item.innerText + " ▼";
      });
    });
  }
</script>